public class Loops {
    public static void main(String[] args) {
        // For , While , Do While

        for (int i = 0; i < 10; i++) {
            System.out.println("Yash " + i);
        }

        for (int i = 10; i > 0; i--) {
            System.out.println(i);
        }

        int j = 0;
        while (j < 10) {
            System.out.println(j);
            j++;
        }
        int k = 0;

        do {
            System.out.println(k);
            k++;
        } while (k < 10);

        int num = 12345;
    }

}
